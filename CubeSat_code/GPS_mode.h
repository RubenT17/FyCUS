// UBX-CFG-NAV5 packet. Select Dynamic Model: 0-Portable, 6-Airborn<1g

#pragma once


void sendPacket(byte *packet, byte packetLen)
{
  for (byte i = 0; i < packetLen; i++)
  {
    Serial1.write(packet[i]);
  }
}



